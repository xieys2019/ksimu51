#include "Gate2ANDW.h"

Gate2ANDW::Gate2ANDW(QWidget* parent, char const * name, int wflags, unsigned int const  componentNumber, unsigned int const  componentType) : Gate2NANDW(parent, name, wflags, componentNumber, componentType)
{
	componentName_.replace(componentName_.length()-10, 4, "AND");
	delayTimeTyp_=DELAYTIME;

	loadComponentXPMImage();
	componentImageQLabel_->setPixmap(componentImage_);

	initW();
}

Gate2ANDW::~Gate2ANDW()
{

}

bool Gate2ANDW::logic2Operation(IOPin* inA, IOPin* inB)
{
	if(inA->readValue(true)&&inB->readValue(true))
		return true;
	else
		return false;
}

