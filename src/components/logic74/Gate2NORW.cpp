#include "Gate2NORW.h"

Gate2NORW::Gate2NORW(QWidget* parent, char const * name, int wflags, unsigned int const  componentNumber, unsigned int const  componentType) : Gate2NANDW(parent, name, wflags, componentNumber, componentType)
{
	componentName_.replace(componentName_.length()-10, 4, "NOR");
	delayTimeTyp_=DELAYTIME;

	loadComponentXPMImage();
	componentImageQLabel_->setPixmap(componentImage_);

	initW();
}

Gate2NORW::~Gate2NORW()
{
}

bool Gate2NORW::logic2Operation(IOPin* inA, IOPin* inB)
{
	if(!inA->readValue(true) && !inB->readValue(true))
		return true;
	else
		return false;
}

